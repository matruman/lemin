int		ft_isalpha(int c)
{
	unsigned char	ch;

	if (c < 0 || c > 127)
		return (0);
	ch = (unsigned char)c;
	if ((ch >= 'A' && ch <= 'Z') || (ch >= 'a' && ch <= 'z'))
		return (1);
	else
		return (0);
}
